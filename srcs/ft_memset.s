; void			*ft_memset(void *mem, int c, size_t len);
global	ft_memset

ft_memset:
	mov		rax, rsi
	mov		rcx, rdx
	mov		r8, rdi		; save rdi
	rep		stosb		; repeat while rcx
	mov		rax, r8		; return rdi
	ret
